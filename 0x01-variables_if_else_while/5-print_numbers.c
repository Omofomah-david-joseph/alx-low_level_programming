#include <stdio.h>

/**
 * main - Program that prints all single digits of base 10
 *
 * Return: Always0 (success)
 */
int main(void)
{
	for (int i = 0; i <= 9; i++)
		if (i = 0 && i <= 9)
			printf("%d\n", i);
	return (0);
}              
