#include <stdio.h>
#include <stdlib.h>
#include <time.h>

/**
 * main - Program that prints all single digits of base 10
 *
 * Return: Always 0 (success)
 */
int main(void)
{
	for (int i = 0; i <= 9; i++){
		printf("%d\n", i);
	}
	return (0);
}              
